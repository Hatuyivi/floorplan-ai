export type GeneratorConfig = {
  githubOwner: string;
  repoName: string;
  appName: string;
  bundleId: string;
  branch: string;
  visibility: "public" | "private";
  includeSigningNotes: boolean;
};

function escapeDoubleQuotes(value: string) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function hereDoc(path: string, lines: string[]) {
  return [`cat > "${path}" <<'EOF'`, ...lines, "EOF", ""];
}

